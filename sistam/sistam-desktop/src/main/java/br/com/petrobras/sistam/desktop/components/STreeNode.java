package br.com.petrobras.sistam.desktop.components;

import javax.swing.Icon;
import javax.swing.tree.DefaultMutableTreeNode;

public abstract class STreeNode extends DefaultMutableTreeNode {

    public STreeNode(Object userObject) {
        super(userObject);
    }

    public abstract Icon getExpandedIcon();
    
    public abstract Icon getCollapsedIcon();
    
}
