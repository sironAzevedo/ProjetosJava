    public void setAgenciamento(Agenciamento agenciamento){
        model.setAgenciamento(agenciamento);
        
        if (agenciamento != null) {
            if(agenciamento.getAgencia() != null){
                comboFusoHorario.setSelectedItem(agenciamento.getAgencia().obterTimezone());
            }
            model.carregarAcompanhamentos();
        }
    } 
    
