    public void adicionarAcompanhamento() {
        Acompanhamento acompanhamento = new Acompanhamento();
        acompanhamento.setAgenciamento(model.getAgenciamento());
        acompanhamento.setData(new Date());
        acompanhamento.setChaveCadastrador(SistamApp.getSistamApp().getCredentialsBean().getLogon());
        acompanhamento.setNomeCadastrador(SistamApp.getSistamApp().getCredentialsBean().getUsername());
        DetalheAcompanhamentoDialog dialog = new DetalheAcompanhamentoDialog(SistamApp.getSistamApp().getMainFrame(), acompanhamento, false);
        dialog.setVisible(true);
        model.carregarAcompanhamentos();
    }

    public void editarAcompanhamento() {
        DetalheAcompanhamentoDialog dialog = new DetalheAcompanhamentoDialog(SistamApp.getSistamApp().getMainFrame(), model.getAcompanhamentoSelecionado(), true);
        dialog.setVisible(true);
        model.carregarAcompanhamentos();
    }
   
    
