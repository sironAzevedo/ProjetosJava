package br.com.petrobras.sistam.common.valueobjects;

import br.com.petrobras.fcorp.common.beans.AbstractBean;
import br.com.petrobras.sistam.common.entity.Servico;
import br.com.petrobras.sistam.common.entity.ServicoHospedagem;
import br.com.petrobras.sistam.common.enums.TipoCentroCusto;
import java.io.Serializable;

/**
 * Filtro de busca para objetos da classe Agenciamento.
 */
public class FiltroEnvioSolicitacaoHospedagem extends AbstractBean implements Serializable {
    
    private static final long serialVersionUID = 1L;
    
    private boolean agenciaMaritima;
    private boolean hotel;
    private Servico agenciaViagem;
    private String lotacao;
    private TipoCentroCusto tipoCentroCusto;
    private ServicoHospedagem servicoHospedagem;

    public boolean isAgenciaMaritima() {
        return agenciaMaritima;
    }

    public void setAgenciaMaritima(boolean agenciaMaritima) {
        this.agenciaMaritima = agenciaMaritima;
    }

    public boolean isHotel() {
        return hotel;
    }

    public void setHotel(boolean hotel) {
        this.hotel = hotel;
    }

    public Servico getAgenciaViagem() {
        return agenciaViagem;
    }

    public void setAgenciaViagem(Servico agenciaViagem) {
        this.agenciaViagem = agenciaViagem;
    }

    public String getLotacao() {
        return lotacao;
    }

    public void setLotacao(String lotacao) {
        this.lotacao = lotacao;
    }

    public TipoCentroCusto getTipoCentroCusto() {
        return tipoCentroCusto;
    }

    public void setTipoCentroCusto(TipoCentroCusto tipoCentroCusto) {
        this.tipoCentroCusto = tipoCentroCusto;
    }

    public ServicoHospedagem getServicoHospedagem() {
        return servicoHospedagem;
    }

    public void setServicoHospedagem(ServicoHospedagem servicoHospedagem) {
        this.servicoHospedagem = servicoHospedagem;
    }
   
}
