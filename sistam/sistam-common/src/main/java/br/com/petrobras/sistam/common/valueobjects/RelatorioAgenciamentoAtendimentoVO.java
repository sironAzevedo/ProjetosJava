package br.com.petrobras.sistam.common.valueobjects;

import br.com.petrobras.sistam.common.entity.Agenciamento;
import java.io.Serializable;
import java.util.Date;

public class RelatorioAgenciamentoAtendimentoVO implements Serializable {

    private Date dataReferencia;
    private Long anoMes;
    private Agenciamento agenciamento;
    private Integer quantidadeDias;
    private Integer quantidadeAtendimentos; 
    
    public Date getDataReferencia() {
        return dataReferencia;
    }

    public void setDataReferencia(Date dataReferencia) {
        this.dataReferencia = dataReferencia;
    }

    public Long getAnoMes() {
        return anoMes;
    }

    public void setAnoMes(Long anoMes) {
        this.anoMes = anoMes;
    }

    public Agenciamento getAgenciamento() {
        return agenciamento;
    }

    public void setAgenciamento(Agenciamento agenciamento) {
        this.agenciamento = agenciamento;
    }

    public Integer getQuantidadeDias() {
        return quantidadeDias;
    }

    public void setQuantidadeDias(Integer quantidadeDias) {
        this.quantidadeDias = quantidadeDias;
    }

    public Integer getQuantidadeAtendimentos() {
        return quantidadeAtendimentos;
    }

    public void setQuantidadeAtendimentos(Integer quantidadeAtendimentos) {
        this.quantidadeAtendimentos = quantidadeAtendimentos;
    } 
}
