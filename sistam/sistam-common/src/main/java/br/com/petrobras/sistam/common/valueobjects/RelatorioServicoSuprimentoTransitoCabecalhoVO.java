package br.com.petrobras.sistam.common.valueobjects;

/**
 *
 */
public class RelatorioServicoSuprimentoTransitoCabecalhoVO {

    private String colunaUm;
    private String colunaDois;

    public RelatorioServicoSuprimentoTransitoCabecalhoVO(String colunaUm, String colunaDois) {
        this.colunaUm = colunaUm;
        this.colunaDois = colunaDois;
    }

    public String getColunaUm() {
        return colunaUm;
    }

    public String getColunaDois() {
        return colunaDois;
    }
}
